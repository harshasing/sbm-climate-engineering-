export const products = categories.flatMap((cat: any) =>
  (cat.subcategories || []).map((sub: any, i: number) => {
    const name = sub.name || `product-${cat.id}-${i}`
    const slug =
      name
        .toString()
        .toLowerCase()
        .replace(/\s+/g, '-')
        .replace(/[^\w-]+/g, '-')
        .replace(/--+/g, '-')
        .replace(/^-+/, '')
        .replace(/-+$/, '') || `p-${cat.id}-${i}`

    return {
      ...sub,
      category: cat.name,
      categoryId: cat.id,
      name,
      slug,
      images: sub.images || (sub.image ? [sub.image] : []),
      image: (sub.images && sub.images[0]) || sub.image || '/logo.webp',
      description: sub.description || `${cat.name} - ${name}`,
      featured: i % 3 === 0,
    }
  }),
)

